package basic;

public class if_else {

    public static void main(String[] args) {
       //conditional stt
        boolean isSunUp = false;
        if (isSunUp == true)
            System.out.println("day");
        else
            System.out.println("night");

        int age = 30;
        if (age > 18)
            System.out.println("can vote");
        else
            System.out.println("can't vote");
    }
}
