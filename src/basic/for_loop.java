package basic;

public class for_loop {

    public static void main(String[] args) {
        //FOR LOOPS

        //1 - 100
        for (int i = 1; i <= 100; i = i + 1){
            System.out.println(i);
        }
        //100 - 1
        for (int j = 100; j >= 1; j = j - 1){
            System.out.println(j);
        }
    }
}